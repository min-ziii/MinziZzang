    public static void entryTitle(String title, int interval, int gapSize) {
        String[] titleArr = title.split("/");
        ArrayList<String> completeLines = new ArrayList<>();

        try {
            for (int i = 0; i < titleArr.length; i++) {
                String[] lines = titleArr[i].split("");
                String temp = "";

                for (int j = 0; j < lines.length; j++) {
                    String words = lines[j];

                    for (int k = 0; k < words.length(); k++) {
                        boolean isLast = i == titleArr.length - 1 && j == lines.length - 1 && k == words.length() - 1;

                        if (!isLast) {
                            printGap(gapSize);
                        }

                        if (!completeLines.isEmpty()) {
                            for (String completeLine : completeLines) {
                                System.out.print(FigletFont.convertOneLine(completeLine));
                            }
                        }


                        temp += words.charAt(k);
                        System.out.print(FigletFont.convertOneLine(temp));


                        pause(interval);

                        if (!isLast) {
                            printGap(gapSize);
                        }
                    }

                }
                completeLines.add(temp);
            }
        } catch (IOException e) {
            throw new RuntimeException(e);
        }
    }

    public static void replayTitle(int interval, int gapSize, int repeat) {
        printGap(5);

        for (int i = 0; i < repeat; i++) {
            try {
                System.out.println(FigletFont.convertOneLine("REPLAY!!"));
                printGap(gapSize);
                pause(interval);

                System.out.println(FigletFont.convertOneLine(""));
                printGap(gapSize);
                pause(interval);
            } catch (IOException e) {
                throw new RuntimeException(e);
            }
        }
    }

