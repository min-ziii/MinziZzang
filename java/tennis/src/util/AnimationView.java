    public static void matchStart(int interval, int gapSize, int repeat) {
        for (int i = 0; i < repeat * 2; i++) {
            try {
                printGap(2);

                if (i % 2 == 0) {
                    System.out.println(FigletFont.convertOneLine("MATCH!!"));
                    printGap(4);
                } else {
                    System.out.println(FigletFont.convertOneLine(" "));
                }

                printGap(2);

                if (i % 2 == 0) {
                    System.out.println(FigletFont.convertOneLine("START!!"));
                    printGap(4);
                } else {
                    System.out.println(FigletFont.convertOneLine(" "));
                }
                pause(interval / 2);
                printGap(gapSize);
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        printGap(gapSize);
    }


